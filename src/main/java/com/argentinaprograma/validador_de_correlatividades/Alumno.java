package com.argentinaprograma.validador_de_correlatividades;



import java.util.ArrayList;

/**
 *
 * @author ALAN
 */
public class Alumno {
   String nombre;
    int legajo;
    ArrayList<String> materiasAprobadas = new ArrayList<>();

    public Alumno(String nombre) {
        this.nombre = nombre;
         this.materiasAprobadas = new ArrayList<>();
    }

    public Alumno() {
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getLegajo() {
        return legajo;
    }

    public void setLegajo(int legajo) {
        this.legajo = legajo;
    }

    public ArrayList<String> getMateriasAprobadas() {
        return materiasAprobadas;
    }

    public void setMateriasAprobadas(ArrayList<String> materiasAprobadas) {
        this.materiasAprobadas = materiasAprobadas;
    }
}